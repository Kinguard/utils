#include "TestConstants.h"

CPPUNIT_TEST_SUITE_REGISTRATION (TestConstants);

#include "Constants.h"

using namespace Utils::Constants;

void TestConstants::setUp(){
}

void TestConstants::tearDown(){
}

void TestConstants::test()
{
	// Decimal/SI
	CPPUNIT_ASSERT_EQUAL( 1000UL, Kilo);
	CPPUNIT_ASSERT_EQUAL( 1000UL, 1_Kilo);
	CPPUNIT_ASSERT_EQUAL( Kilo, 1_Kilo);

	CPPUNIT_ASSERT_EQUAL( 1000000UL, Mega);
	CPPUNIT_ASSERT_EQUAL( 1000000UL, 1_Mega);
	CPPUNIT_ASSERT_EQUAL( Mega, 1_Mega);

	CPPUNIT_ASSERT_EQUAL( 1000000000UL, Giga);
	CPPUNIT_ASSERT_EQUAL( 1000000000UL, 1_Giga);
	CPPUNIT_ASSERT_EQUAL( Giga, 1_Giga);

	CPPUNIT_ASSERT_EQUAL( 1000000000000UL, Tera);
	CPPUNIT_ASSERT_EQUAL( 1000000000000UL, 1_Tera);
	CPPUNIT_ASSERT_EQUAL( Tera, 1_Tera);

	// Binary
	CPPUNIT_ASSERT_EQUAL( 1024UL, KB);
	CPPUNIT_ASSERT_EQUAL( 1024UL, 1_KB);
	CPPUNIT_ASSERT_EQUAL( KB, 1_KB);

	CPPUNIT_ASSERT_EQUAL( 1048576UL, MB);
	CPPUNIT_ASSERT_EQUAL( 1048576UL, 1_MB);
	CPPUNIT_ASSERT_EQUAL( MB, 1_MB);

	CPPUNIT_ASSERT_EQUAL( 1073741824UL, GB);
	CPPUNIT_ASSERT_EQUAL( 1073741824UL, 1_GB);
	CPPUNIT_ASSERT_EQUAL( GB, 1_GB);

	CPPUNIT_ASSERT_EQUAL( 1099511627776UL, TB);
	CPPUNIT_ASSERT_EQUAL( 1099511627776UL, 1_TB);
	CPPUNIT_ASSERT_EQUAL( TB, 1_TB);
}
