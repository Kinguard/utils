#include "TestConstants.h"

CPPUNIT_TEST_SUITE_REGISTRATION (TestConstants);

#include "Constants.h"

using namespace Utils::Constants;

void TestConstants::setUp(){
}

void TestConstants::tearDown(){
}

void TestConstants::test()
{
	// Decimal/SI
	CPPUNIT_ASSERT_EQUAL( 1000ULL, Kilo);
	CPPUNIT_ASSERT_EQUAL( 1000ULL, 1_Kilo);
	CPPUNIT_ASSERT_EQUAL( Kilo, 1_Kilo);

	CPPUNIT_ASSERT_EQUAL( 1000000ULL, Mega);
	CPPUNIT_ASSERT_EQUAL( 1000000ULL, 1_Mega);
	CPPUNIT_ASSERT_EQUAL( Mega, 1_Mega);

	CPPUNIT_ASSERT_EQUAL( 1000000000ULL, Giga);
	CPPUNIT_ASSERT_EQUAL( 1000000000ULL, 1_Giga);
	CPPUNIT_ASSERT_EQUAL( Giga, 1_Giga);

	CPPUNIT_ASSERT_EQUAL( 1000000000000ULL, Tera);
	CPPUNIT_ASSERT_EQUAL( 1000000000000ULL, 1_Tera);
	CPPUNIT_ASSERT_EQUAL( Tera, 1_Tera);

	// Binary
	CPPUNIT_ASSERT_EQUAL( 1024ULL, KB);
	CPPUNIT_ASSERT_EQUAL( 1024ULL, 1_KB);
	CPPUNIT_ASSERT_EQUAL( KB, 1_KB);

	CPPUNIT_ASSERT_EQUAL( 1048576ULL, MB);
	CPPUNIT_ASSERT_EQUAL( 1048576ULL, 1_MB);
	CPPUNIT_ASSERT_EQUAL( MB, 1_MB);

	CPPUNIT_ASSERT_EQUAL( 1073741824ULL, GB);
	CPPUNIT_ASSERT_EQUAL( 1073741824ULL, 1_GB);
	CPPUNIT_ASSERT_EQUAL( GB, 1_GB);

	CPPUNIT_ASSERT_EQUAL( 1099511627776ULL, TB);
	CPPUNIT_ASSERT_EQUAL( 1099511627776ULL, 1_TB);
	CPPUNIT_ASSERT_EQUAL( TB, 1_TB);
}
